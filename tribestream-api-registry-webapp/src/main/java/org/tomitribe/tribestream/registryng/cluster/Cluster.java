package org.tomitribe.tribestream.registryng.cluster;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Documented
@Target({ElementType.TYPE})
@Retention(RetentionPolicy.RUNTIME)
/**
 * This annotation aims at being used to mark event POJOs that have to be sent through a broker, so that other cluster
 * instances can receive a notification and do something (or the same).
 *
 * The group to which the annotated event will be broadcasted is for now identified using the system property
 * <strong>tribe.cluster.group.name</strong>
 *
 */
public @interface Cluster {
}
